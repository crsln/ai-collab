# init command — interactive setup wizard
# ---------------------------------------------------------------------------

def _cmd_init(args: argparse.Namespace) -> None:
    non_interactive = args.non_interactive

    # ── Step 1: Welcome ──────────────────────────────────────────────
    print()
    print("=" * 64)
    print("  ai-collab Setup Wizard")
    print("=" * 64)
    print()
    print("ai-collab orchestrates multi-AI brainstorming sessions.")
    print("Multiple AI agents (Copilot, Gemini, Codex, Aider, etc.)")
    print("collaborate through a shared SQLite database, with Claude")
    print("Code acting as the orchestrator.")
    print()
    print("This wizard will:")
    print("  1. Detect installed AI CLI tools")
    print("  2. Let you choose which agents to enable")
    print("  3. Generate ai-collab.toml configuration")
    print("  4. Show MCP config snippets for each tool")
    print("  5. Seed the database with defaults")
    print()

    if not non_interactive:
        if not _prompt_yes_no("Continue?"):
            print("Aborted.")
            return

    # ── Step 2: Detect CLIs ──────────────────────────────────────────
    print()
    print("Scanning PATH for AI CLI tools...")
    print()

    detected = _detect_agents()

    # Display results
    found_any = False
    for key, path in detected.items():
        if key == "claude":
            label = f"{ORCHESTRATOR['display_name']} ({ORCHESTRATOR['note']})"
        else:
            label = KNOWN_AGENTS[key]["display_name"]

        if path:
            found_any = True
            print(f"  [x] {label}")
            print(f"      {path}")
        else:
            print(f"  [ ] {label}  (not found)")

    print()

    if not found_any:
        print("No AI CLI tools were detected on PATH.")
        print("You can still configure agents manually.")
        print()

    # ── Step 3: Agent selection ──────────────────────────────────────
    # Build list of agents to enable
    enabled_agents: list[dict] = []
    detected_keys = [k for k in KNOWN_AGENTS if detected.get(k)]

    if non_interactive:
        # Non-interactive: enable all detected agents with defaults
        for key in detected_keys:
            info = KNOWN_AGENTS[key]
            enabled_agents.append({
                "name": key,
                "command": info["command"],
                "args": list(info["args"]),
                "display_name": info["display_name"],
                "description": info["description"],
                "enabled": True,
                "model": None,
                "timeout": None,
            })
        print(f"Non-interactive mode: enabling {len(enabled_agents)} detected agent(s).")
    else:
        # Interactive: let user pick from detected agents
        if detected_keys:
            print("Select which detected agents to enable:")
            print()
            for key in detected_keys:
                info = KNOWN_AGENTS[key]
                enable = _prompt_yes_no(f"  Enable {info['display_name']}?")
                if enable:
                    enabled_agents.append({
                        "name": key,
                        "command": info["command"],
                        "args": list(info["args"]),
                        "display_name": info["display_name"],
                        "description": info["description"],
                        "enabled": True,
                        "model": None,
                        "timeout": None,
                    })
            print()

        # Offer to add undetected known agents
        undetected_keys = [k for k in KNOWN_AGENTS if not detected.get(k)]
        if undetected_keys:
            if _prompt_yes_no("Add any agents that were not detected on PATH?", default=False):
                for key in undetected_keys:
                    info = KNOWN_AGENTS[key]
                    if _prompt_yes_no(f"  Add {info['display_name']} (not detected)?", default=False):
                        cmd = input(f"    Command [{info['command']}]: ").strip() or info["command"]
                        enabled_agents.append({
                            "name": key,
                            "command": cmd,
                            "args": list(info["args"]),
                            "display_name": info["display_name"],
                            "description": info["description"],
                            "enabled": True,
                            "model": None,
                            "timeout": None,
                        })
                print()

        # Offer to add fully custom agents
        while True:
            if not _prompt_yes_no("Add a custom agent definition?", default=False):
                break
            name = input("  Agent name (short identifier, lowercase letters/numbers/-/_): ").strip()
            if not name:
                print("  Skipped (no name given).")
                continue
            if not _VALID_AGENT_NAME.match(name):
                print(f"  Invalid name '{name}': must match [a-z][a-z0-9_-]* (e.g. 'my-agent')")
                continue
            command = input("  Command to run: ").strip()
            if not command:
                print("  Skipped (no command given).")
                continue
            args_raw = input('  Args pattern (comma-separated, use {prompt} placeholder) ["-p","{prompt}"]: ').strip()
            if args_raw:
                agent_args = [a.strip().strip('"').strip("'") for a in args_raw.split(",")]
            else:
                agent_args = ["-p", "{prompt}"]
            display = input(f"  Display name [{name.title()}]: ").strip() or name.title()
            desc = input("  Description: ").strip() or f"Custom agent: {name}"
            enabled_agents.append({
                "name": name,
                "command": command,
                "args": agent_args,
                "display_name": display,
                "description": desc,
                "enabled": True,
                "model": None,
                "timeout": None,
            })
            print(f"  Added {display}.")
            print()

    if not enabled_agents:
        print("No agents enabled. You can re-run 'ai-collab init' later.")
        print()

    # ── Step 4: Generate ai-collab.toml ──────────────────────────────
    if enabled_agents:
        if _CONFIG_FILE.exists() and not non_interactive:
            if not _prompt_yes_no(f"Overwrite existing {_CONFIG_FILE.name}?"):
                print("  Keeping existing config.")
            else:
                _write_toml(enabled_agents, _CONFIG_FILE)
                print(f"  Wrote {_CONFIG_FILE}")
        else:
            _write_toml(enabled_agents, _CONFIG_FILE)
            print(f"  Wrote {_CONFIG_FILE}")
        print()

    # ── Step 5: MCP config snippets ──────────────────────────────────
    agent_names = [a["name"] for a in enabled_agents]
    _print_mcp_config(agent_names)

    # ── Step 6: Seed database ────────────────────────────────────────
    print()
    do_seed = non_interactive or _prompt_yes_no("Seed database with default agent definitions, workflows, and tool guides?")
    if do_seed:
        print("Seeding database...")
        counts = _run_seed()
        print(f"  Seeded: {counts['agents']} agent definitions, "
              f"{counts['workflows']} workflow templates, "
              f"{counts['tools']} tool guides")
        print(f"  Database: {_DB_PATH}")
    print()

    # ── Step 7: Summary ──────────────────────────────────────────────
    print("=" * 64)
    print("  Setup Complete")
    print("=" * 64)
    print()
    if enabled_agents:
        print(f"Enabled agents ({len(enabled_agents)}):")
        for a in enabled_agents:
            status = "enabled" if a["enabled"] else "disabled"
            print(f"  - {a['display_name']} ({a['name']}) [{status}]")
    else:
        print("No agents configured.")
    print()
    print("Next steps:")
    print("  1. Copy the MCP config snippets above into the appropriate files")
    print("  2. Restart Claude Code (and other agents) to pick up the new config")
    print("  3. Run a brainstorm: use /multi-ai-brainstorm or the bs_* MCP tools")
    print()
    if not do_seed:
        print("  Tip: Run 'ai-collab seed' to populate the database with defaults.")
        print()


