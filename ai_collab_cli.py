def _run_seed() -> dict:
    """Seed the brainstorm DB with default agent definitions, workflow templates,
    and tool guides. Returns counts of seeded records.
    """
    from brainstorm_db import BrainstormDB
    from brainstorm_seeds import AGENT_DEFINITIONS, TOOL_GUIDES, WORKFLOW_TEMPLATES

    db = BrainstormDB(_DB_PATH)
    counts = {"agents": 0, "workflows": 0, "tools": 0}
    try:
        for defn in AGENT_DEFINITIONS:
            db.upsert_agent_definition(**defn)
            counts["agents"] += 1
        for wf in WORKFLOW_TEMPLATES:
            phases_json = json.dumps(wf["phases"])
            db.upsert_workflow_template(
                name=wf["name"],
                overview=wf["overview"],
                phases_json=phases_json,
                convergence_rules=wf["convergence_rules"],
                response_format=wf["response_format"],
            )
            counts["workflows"] += 1
        for guide in TOOL_GUIDES:
            db.upsert_tool_guide(**guide)
            counts["tools"] += 1
    finally:
        db.close()
    return counts


