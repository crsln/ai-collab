def _mcp_snippet_claude() -> dict:
    """JSON snippet for Claude Code's MCP settings (mcp_server.py)."""
    server_path = str(_PROJECT_DIR / "mcp_server.py")
    return {
        "ai-collab": {
            "type": "stdio",
            "command": "python",
            "args": [server_path],
        }
    }


def _mcp_snippet_agent(agent_name: str) -> dict:
    """JSON snippet for an agent's MCP config (brainstorm_server.py)."""
    server_path = str(_PROJECT_DIR / "brainstorm_server.py")
    return {
        "brainstorm": {
            "type": "stdio",
            "command": "python",
            "args": [server_path],
        }
    }


def _print_mcp_config(enabled_agents: list[str]) -> None:
    """Print MCP configuration snippets to stdout."""
    print()
    print("=" * 64)
    print("MCP Configuration Snippets")
    print("=" * 64)

    # Claude Code config
    print()
    print("1) Claude Code  (add to your MCP settings)")
    print("   Location: ~/.claude.json  or  project .mcp.json")
    print()
    snippet = _mcp_snippet_claude()
    print(json.dumps(snippet, indent=2))

    # Agent configs
    config_locations = {
        "copilot": "~/.copilot/mcp-config.json",
        "gemini": ".gemini/settings.json (project-level)",
        "codex": "codex MCP config (see codex docs)",
        "aider": "aider MCP config (see aider docs)",
    }

    for agent in enabled_agents:
        if agent == "claude":
            continue
        loc = config_locations.get(agent, f"{agent} MCP config")
        print()
        print(f"2) {KNOWN_AGENTS.get(agent, {}).get('display_name', agent)}  (add brainstorm server)")
        print(f"   Location: {loc}")
        print()
        snippet = _mcp_snippet_agent(agent)
        print(json.dumps(snippet, indent=2))

    print()
    print("-" * 64)
    print("Copy the JSON above into the appropriate config files.")
    print("All agents share the same brainstorm.db via brainstorm_server.py.")
    print("-" * 64)


