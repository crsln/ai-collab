# agents command
# ---------------------------------------------------------------------------

def _cmd_agents(args: argparse.Namespace) -> None:
    # 1. Show agents from config file (if exists)
    config = _read_toml(_CONFIG_FILE)
    config_agents: list[dict] = []
    if config and "agents" in config:
        agents_data = config["agents"]
        if isinstance(agents_data, dict):
            # [agents.NAME] format → dict of dicts
            for name, data in agents_data.items():
                if isinstance(data, dict):
                    data["name"] = name
                    config_agents.append(data)
        elif isinstance(agents_data, list):
            # [[agents]] format → list of dicts (legacy)
            config_agents = agents_data

    # 2. Show agents from DB (if seeded)
    db_agents: list[dict] = []
    if _DB_PATH.exists():
        try:
            from brainstorm_db import BrainstormDB
            db = BrainstormDB(_DB_PATH)
            db_agents = db.list_agent_definitions()
            db.close()
        except Exception:
            pass

    # 3. Detect on PATH
    detected = _detect_agents()

    print()
    print("=" * 64)
    print("  ai-collab Agents")
    print("=" * 64)
    print()

    # Config file agents
    if config_agents:
        print(f"From ai-collab.toml ({len(config_agents)} configured):")
        for a in config_agents:
            name = a.get("name", "?")
            display = a.get("display_name", name)
            enabled = a.get("enabled", True)
            cmd = a.get("command", "?")
            on_path = _find_cli(cmd) is not None
            status_parts = []
            if enabled:
                status_parts.append("enabled")
            else:
                status_parts.append("disabled")
            if on_path:
                status_parts.append("on PATH")
            else:
                status_parts.append("NOT on PATH")
            print(f"  {display} ({name}): {', '.join(status_parts)}")
            print(f"    command: {cmd}")
            desc = a.get("description", "")
            if desc:
                print(f"    description: {desc}")
        print()
    else:
        print("No agents configured in ai-collab.toml.")
        if not _CONFIG_FILE.exists():
            print("  Run 'ai-collab init' to set up.")
        print()

    # DB agents
    if db_agents:
        print(f"From database ({len(db_agents)} agent definitions):")
        for a in db_agents:
            print(f"  {a['display_name']} ({a['agent_name']})")
            cap = a.get("capabilities", "")
            if cap:
                # Truncate long capabilities
                if len(cap) > 100:
                    cap = cap[:97] + "..."
                print(f"    capabilities: {cap}")
        print()
    else:
        if _DB_PATH.exists():
            print("No agent definitions in database. Run 'ai-collab seed'.")
        else:
            print("Database not initialized. Run 'ai-collab init' or 'ai-collab seed'.")
        print()

    # PATH detection
    print("CLI detection (current PATH):")
    for key, path in detected.items():
        if key == "claude":
            label = ORCHESTRATOR["display_name"]
        else:
            label = KNOWN_AGENTS[key]["display_name"]
        if path:
            print(f"  [x] {label}: {path}")
        else:
            print(f"  [ ] {label}: not found")
    print()


