def _find_cli(name: str) -> str | None:
    """Find a CLI executable on PATH. On Windows, also check .cmd variants."""
    if sys.platform == "win32":
        found = shutil.which(f"{name}.cmd") or shutil.which(name)
    else:
        found = shutil.which(name)
    return found


def _detect_agents() -> dict[str, str | None]:
    """Scan PATH for all known agent CLIs plus the orchestrator.

    Returns a dict mapping agent key -> resolved path (or None if not found).
    """
    detected: dict[str, str | None] = {}
    for key in KNOWN_AGENTS:
        detected[key] = _find_cli(KNOWN_AGENTS[key]["command"])
    detected["claude"] = _find_cli(ORCHESTRATOR["command"])
    return detected


def _prompt_yes_no(question: str, default: bool = True) -> bool:
    suffix = " [Y/n] " if default else " [y/N] "
    while True:
        answer = input(question + suffix).strip().lower()
        if answer == "":
            return default
        if answer in ("y", "yes"):
            return True
        if answer in ("n", "no"):
            return False
        print("  Please answer y or n.")


def _prompt_choice(prompt_text: str, options: list[str], default: str | None = None) -> str:
    print(prompt_text)
    for i, opt in enumerate(options, 1):
        marker = " (default)" if opt == default else ""
        print(f"  {i}. {opt}{marker}")
    while True:
        raw = input("Choice: ").strip()
        if raw == "" and default is not None:
            return default
        try:
            idx = int(raw)
            if 1 <= idx <= len(options):
                return options[idx - 1]
        except ValueError:
            if raw in options:
                return raw
        print(f"  Enter a number 1-{len(options)} or one of: {', '.join(options)}")


def _write_toml(agents: list[dict], path: Path) -> None:
    """Write ai-collab.toml with [agents.NAME] table format.

    We hand-write TOML to avoid depending on any external library for writing.
    Uses [agents.NAME] table format (not [[agents]] array of tables) to match
    what config.py expects.
    """
    lines = [
        "# ai-collab configuration",
        "# Generated by: ai-collab init",
        f"# Platform: {platform.system()} {platform.release()}",
        "",
        "[settings]",
        'db_path = ".data/brainstorm.db"',
        "default_timeout = 900",
        "",
    ]

    for agent in agents:
        name = agent["name"]
        if not _VALID_AGENT_NAME.match(name):
            raise ValueError(f"Invalid agent name '{name}': must match [a-z][a-z0-9_-]*")
        lines.append(f"[agents.{name}]")
        lines.append(f'enabled = {str(agent["enabled"]).lower()}')
        lines.append(f'command = "{agent["command"]}"')

        # args as a TOML array
        args_str = ", ".join(f'"{a}"' for a in agent["args"])
        lines.append(f"args = [{args_str}]")

        lines.append(f'display_name = "{agent["display_name"]}"')

        # Escape any double quotes in description
        desc = agent["description"].replace('"', '\\"')
        lines.append(f'description = "{desc}"')

        if agent.get("model"):
            lines.append(f'model = "{agent["model"]}"')
        if agent.get("timeout"):
            lines.append(f"timeout = {agent['timeout']}")

        lines.append("")

    path.write_text("\n".join(lines), encoding="utf-8")


def _read_toml(path: Path) -> dict | None:
    """Read a TOML file. Returns parsed dict or None on failure."""
    if not path.exists():
        return None
    try:
        # Python 3.11+
        import tomllib
        with open(path, "rb") as f:
            return tomllib.load(f)
    except ImportError:
        pass
    # Fallback: try tomli (pip install tomli)
    try:
        import tomli  # type: ignore[import-untyped]
        with open(path, "rb") as f:
            return tomli.load(f)
    except ImportError:
        pass
    # Last resort: basic manual parse for our simple format
    return None


